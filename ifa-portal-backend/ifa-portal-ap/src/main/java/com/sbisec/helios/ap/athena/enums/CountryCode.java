    public static CountryCode getById(String id) {
        if (ObjectUtils.isEmpty(id)) {
            return null;
        }

        CountryCode[] enums = values();

        for (int i = 0; i < enums.length; i++) {
            if (enums[i].getId().equals(id)) {
                return enums[i];
            }
        }

        return null;
    }

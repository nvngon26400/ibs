    public static CurrencyCode getById(String id) {
        if (ObjectUtils.isEmpty(id)) {
            return null;
        }

        CurrencyCode[] enums = values();

        for (int i = 0; i < enums.length; i++) {
            if (enums[i].getId().equals(id)) {
                return enums[i];
            }
        }

        return null;
    }
